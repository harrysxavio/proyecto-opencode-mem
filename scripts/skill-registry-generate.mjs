#!/usr/bin/env node
import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { repoRoot } from "./manifest-utils.mjs";

function toPosix(value) {
  return value.split(path.sep).join("/");
}

function parseFrontmatter(text) {
  if (!text.startsWith("---\n")) return {};
  const end = text.indexOf("\n---", 4);
  if (end === -1) return {};
  const block = text.slice(4, end).trim();
  const fields = {};
  for (const line of block.split(/\r?\n/)) {
    const match = /^([A-Za-z0-9_-]+):\s*(.*)$/.exec(line);
    if (match) fields[match[1]] = match[2].trim().replace(/^["']|["']$/g, "");
  }
  return fields;
}

export async function generateSkillRegistry(root = repoRoot) {
  const skillsDir = path.join(root, "skills");
  const entries = await readdir(skillsDir, { withFileTypes: true });
  const skills = [];

  for (const entry of entries) {
    if (!entry.isDirectory() || entry.name === "examples") continue;
    const skillPath = path.join(skillsDir, entry.name, "SKILL.md");
    const text = await readFile(skillPath, "utf8").catch(() => null);
    if (!text) continue;
    const frontmatter = parseFrontmatter(text);
    skills.push({
      name: frontmatter.name || entry.name,
      description: frontmatter.description || "No description provided",
      path: toPosix(path.relative(root, skillPath))
    });
  }

  skills.sort((a, b) => a.name.localeCompare(b.name));

  const lines = [
    "# Skill Registry",
    "",
    "> Generated by `scripts/skill-registry-generate.mjs`. Do not add private paths.",
    "",
    "| Skill | Trigger / description | Path |",
    "|---|---|---|",
    ...skills.map((skill) => `| ${skill.name} | ${skill.description} | ${skill.path} |`),
    ""
  ];

  const outDir = path.join(root, ".atl");
  await mkdir(outDir, { recursive: true });
  await writeFile(path.join(outDir, "skill-registry.md"), lines.join("\n"), "utf8");
  return { count: skills.length, skills };
}

if (process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  const result = await generateSkillRegistry();
  console.log(`Skill registry generated: ${result.count} skills`);
}
